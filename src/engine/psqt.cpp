int psqt_score(const Board& b) {

    int score = 0;

    const int* tables[6] = {
        pawn_table,
        knight_table,
        bishop_table,
        rook_table,
        queen_table,
        king_table
    };

    for (int piece = PAWN; piece <= KING; piece++) {

        // WHITE
        Bitboard bb = b.bb_pieces[WHITE][piece];
        while (bb) {
            int sq = __builtin_ctzll(bb);
            score += tables[piece][sq];
            bb &= bb - 1;
        }

        // BLACK
        bb = b.bb_pieces[BLACK][piece];
        while (bb) {
            int sq = __builtin_ctzll(bb);
            score -= tables[piece][mirror_square(sq)];
            bb &= bb - 1;
        }
    }

    return score;
}
